package exercicio2;

public class Matematica {
	
	
	//--------------------------------------------------------------------------------------------

	public double soma(double v1, double v2) {
		double resultado;
		resultado = v1 + v2;
		return resultado;
	}
	
	public int soma(int v1, int v2) {
		int resultado;
		resultado = v1 + v2;
		return resultado;
	}
	
	public int soma(int v1, int v2, int v3, int v4) {
		int resultado;
		resultado = v1 + v2 + v3 + v4;
		return resultado;
	}
	
	
	
	public double subtracao(double v1, double v2) {
		double resultado;
		resultado = v1 - v2;
		return resultado;
	}
	
	public int subtracao(int v1, int v2) {
		int resultado;
		resultado = v1 - v2;
		return resultado;
	}
	
	public int subtracao(int v1, int v2, int v3, int v4) {
		int resultado;
		resultado = v1 - v2 - v3 - v4;
		return resultado;
	}
	
	//--------------------------------------------------------------------------------------------
	
	

}
