package exercicio1;

public class Calculos {

	//---------------------------------------------------------------------------------------
	
	public double soma(double v1, double v2) {
		double resultado;
		resultado = v1 + v2;
		return resultado;
	}
	
	public double subtracao(double v1, double v2) {
		double resultado;
		resultado = v1 - v2;
		return resultado;
	}
	
	public double divisao(double v1, double v2) {
		double resultado;
		resultado = v1 / v2;
		return resultado;
	}
	
	public double multiplicacao(double v1, double v2) {
		double resultado;
		resultado = v1 * v2;
		return resultado;
	}
	
	//---------------------------------------------------------------------------------------

	
}
